class MySQLObject
{
    // -------------------------------------------------------------------------------------------


    public function __construct()
    {
    }
    public function __destruct()
    {
    }
    // -------------------------------------------------------------------------------------------

    public static function print_obj_header($obj)
    {
        echo '<tr>';
        foreach($obj as $k=>$v)
        {
            echo '<th>'.$k.'</th>';
        }
        echo '</tr>';
    }
  // -------------------------------------------------------------------------------------------

    public static function print_obj($obj)
    {
        echo '<tr>';
        foreach($obj as $k=>$v)
        {
            echo '<td>'.$v.'</td>';
        }
        echo '</tr>';
    }
 // -------------------------------------------------------------------------------------------

 
